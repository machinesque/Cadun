    public String getDataHoje() {

        GregorianCalendar calendar = new GregorianCalendar();
        Date data = calendar.getTime();

        String mes = "";
        String dia = "";
        int diaAux = data.getDate();
        String ano = (data.getYear() + 1900 + "");
        int mesAux = data.getMonth() + 1;

        if (diaAux < 10) {
            dia = "0" + diaAux;
        } else {
            dia = "" + diaAux;
        }

        if (mesAux < 10) {
            mes = "0" + mesAux;
        } else {
            mes = "" + mesAux;
        }

        return dia + mes + ano;

    }

