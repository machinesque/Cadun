    public void cancelar() {

        DefaultTableModel modelo = (DefaultTableModel) jtbBuscaCmei.getModel();

        jtfBuscaNomeEntidade.setText("");
        jtfBuscaCoordenadorAtual.setText("");
        jtfBuscaBairro.setText("");

        modelo.setRowCount(0);

    }

    public void pesquisar() {

        DefaultTableModel modelo = (DefaultTableModel) jtbBuscaCmei.getModel();
        iniciaDao();
        //DAO dao = new DAO();
        Cmei cme;
        List listaCmei = new ArrayList();
        String nomeEntid;
        String bairr;
        String coordenadorAtua;

        if (jtfBuscaNomeEntidade.getText().isEmpty()) {
            nomeEntid = "";
        } else {
            nomeEntid = jtfBuscaNomeEntidade.getText();
        }
        if (jtfBuscaBairro.getText().equals("")) {
            bairr = "";
        } else {
            bairr = jtfBuscaBairro.getText();
        }
        if (jtfBuscaCoordenadorAtual.getText().equals("")) {
            coordenadorAtua = "";
        } else {
            coordenadorAtua = jtfBuscaCoordenadorAtual.getText();
        }

        listaCmei = (List) dao.listarCmeisCadastrados(nomeEntid, bairr, coordenadorAtua);

        modelo.setRowCount(0);

        for (int i = 0; i < listaCmei.size(); i++) {

            cme = (Cmei) listaCmei.get(i);

            modelo.addRow(new String[]{String.valueOf(cme.getNomeEntidade()), String.valueOf(cme.getCoordenadorAtual()), String.valueOf(cme.getBairro()), String.valueOf(cme.getEndereco()), String.valueOf(cme.getTelefone())});

        }

        if (jtbBuscaCmei.getRowCount() > 0) {
            jtbBuscaCmei.getSelectionModel().setSelectionInterval(0, 0);
        } else {
            iniciaClasseGeral();
            
            classeGeral.msgInforma("N√£o h√° CMEI's com estes dados!");
        }

    }

    public void buscaDados() {

        iniciaDao();
        iniciaCmei();
        //DAO dao = new DAO();

        String nomeCme;
        String bairr;
        String coordenadorAtua;
        String enderec;

        nomeCme = (String) jtbBuscaCmei.getValueAt(jtbBuscaCmei.getSelectedRow(), 0);
        coordenadorAtua = (String) jtbBuscaCmei.getValueAt(jtbBuscaCmei.getSelectedRow(), 1);
        bairr = (String) jtbBuscaCmei.getValueAt(jtbBuscaCmei.getSelectedRow(), 2);
        enderec = (String) jtbBuscaCmei.getValueAt(jtbBuscaCmei.getSelectedRow(), 3);

        cmei = (Cmei) dao.buscaCmei(nomeCme, bairr, coordenadorAtua, enderec);

    }

