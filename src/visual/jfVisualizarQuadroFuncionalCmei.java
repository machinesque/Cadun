    public void preencheComboCmeis() {

        DAO dao = new DAO();
        List listaCmeis = new ArrayList();

        jcbNomeEntidade.removeAllItems();
        jcbNomeEntidade.addItem("");

        listaCmeis = dao.listarCmeis();

        for (int i = 0; i < listaCmeis.size(); i++) {
            jcbNomeEntidade.addItem(listaCmeis.get(i));
        }

    }

    public void desabilitaCampos() {

        jtfCapacidadeAlunos.setEditable(false);
        jtfVagasDisponiveis.setEditable(false);

    }

    public void buscaCmei() {

        DAO dao = new DAO();
        Cmei cmei;
        int codigoAux;

        cmei = (Cmei) dao.buscaCmei(jcbNomeEntidade.getSelectedItem().toString(), "", "", "");

        jtfCapacidadeAlunos.setText(cmei.getCapacidadeAlunos());
        //jtfCoordenador.setText(cmei.getCoordenadorAtual());
        jlbEstagiarios.setText("" + cmei.getNumeroEstagiarios());
        jlbNumeroLotacoes.setText("" + (cmei.getNumeroLotacoes() + cmei.getNumeroEstagiarios()));
        jlbServidoresConcursados.setText("" + cmei.getNumeroLotacoes());
        jtfVagasDisponiveis.setText("" + cmei.getVagasDisponiveis());

        codigo = cmei.getCodigoCmei();

    }

