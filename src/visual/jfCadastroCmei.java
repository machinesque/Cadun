    private void jtfCapacidadeAlunosFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jtfCapacidadeAlunosFocusLost
        
        if (!jtfCapacidadeAlunos.getText().isEmpty()) {
            ClasseGeral cg = new ClasseGeral();

            jtfCapacidadeAlunos.setText(cg.somenteNumero(jtfCapacidadeAlunos.getText()));

            if (opcao.equals("alterar")) {

                if (capacidadeAlunos < Integer.valueOf(jtfCapacidadeAlunos.getText())) {

                    jtfVagasDisponiveis.setText("" + ((Integer.valueOf(jtfCapacidadeAlunos.getText()) - capacidadeAlunos) + capacidadeVagasDisponiveis));

                } else {

                    jtfVagasDisponiveis.setText("" + (capacidadeVagasDisponiveis - (capacidadeAlunos - Integer.valueOf(jtfCapacidadeAlunos.getText()))));

                }

            } else {
                jtfVagasDisponiveis.setText(jtfCapacidadeAlunos.getText());
            }
        }
        
    }//GEN-LAST:event_jtfCapacidadeAlunosFocusLost

